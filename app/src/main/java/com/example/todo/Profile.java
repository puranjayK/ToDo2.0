package com.example.todo;

public class Profile {
    private int id;
    private String name;
    private String email;
    private String username;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getUsername() {
        return username;
    }
}
